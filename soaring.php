        <p align=center><img src="content/soaring_tsp2.jpg" width="100%"></p>
        </td>
        <td>
        <p>Soaring is probably the most exciting form of flight. Instead of avoiding the sometimes extreme vertical movements of the atmosphere, soaring is about understanding them and harnessing their power. Incredible, hard-to-imagine feats are possible in gliders and the nearby Eastern Sierras are world-renown for their soar ability.</p>
        </td>
    <tr>
        <td>
        <p align=center><img align=left src="content/sierra_wave2.jpg" width="100%"></p>
        <p align=center>Sierra Mountain Wave, visible due to moisture</p>
        </td>
        <td>
        <p>There are 4 main forms of lift in soaring: ridge lift where air is forced up by a slope (common form for <a href="http://adammreeder.com/interests/paragliding/">paragliders</a>), convergence where air masses meet (often terrain induced), thermals where blooms of air rise due to heat, and wave lift produced by downstream waves from disturbance by mountains (think of ripples downstream of rocks in a river). The Eastern Sierras are notorious for their mountain wave conditions. While transiting powered aircraft are often hit by intense turbulence, glider pilots seek out the upward moving air on the front edge of the wave to climb to spectacular airliner altitudes, all without an engine.</p>
       </td>
    </tr>
    <tr>
        <td>
        <p align=center><img align=right src="content/soaring_tsp1.jpg" width="100%"></p>
        </td>
        <td>
        <p>Just 45 minutes from Bakersfield, Tehachapi is known for <a href="http://skylarknorth.com/index.html" target="_blank">Skylark North</a> at <a href="https://en.wikipedia.org/wiki/Mountain_Valley_Airport" target="_blank">Mountain Valley Airport</a> (L94) and the fantastic flights it produces. Air from the Sierra Nevadas, Tehachapis, and Mojave Desert converge to form the Tehachapi convergence line, visible with enough moisture. Just an interesting fact but nearly all NASA Space Shuttle pilots who came from the Air Force have received glider training at Mountain Valley Airport due to instruction they would have received with in Test Pilot School.</p>
        </td>
